    <p class="lead">
        Esta Política de Privacidade explica como tratamos as informações fornecidas durante a navegação no site e no processo de inscrição do Acampamento Juvenil.
    </p>

    <h2>1. Dados coletados</h2>
    <p>
        Podemos coletar dados pessoais informados voluntariamente no formulário de inscrição, como nome, contato, idade, endereço, informações de saúde, preferências, dados de pagamento e demais informações necessárias para organizar a participação no acampamento.
    </p>

    <h2>2. Uso das informações</h2>
    <p>
        Utilizamos os dados para processar inscrições, confirmar pagamentos, organizar grupos, prestar suporte ao campista, enviar comunicações importantes e garantir segurança durante o evento.
    </p>

    <h2>3. Compartilhamento</h2>
    <p>
        As informações podem ser acessadas pela equipe organizadora e por prestadores envolvidos na realização do acampamento quando isso for necessário para a operação do evento. Não vendemos dados pessoais.
    </p>

    <h2>4. Cookies</h2>
    <p>
        O site usa cookies necessários para funcionamento da sessão, proteção contra requisições indevidas e memorização do aceite do aviso de cookies. O cookie de consentimento é identificado como <strong>juvenil_cookie_consent</strong>.
    </p>

    <h2>5. Segurança</h2>
    <p>
        Adotamos medidas razoáveis de proteção para reduzir riscos de acesso não autorizado, alteração, perda ou uso indevido das informações. Nenhum sistema é completamente imune a falhas, mas buscamos tratar os dados com cuidado e responsabilidade.
    </p>

    <h2>6. Retenção</h2>
    <p>
        Os dados podem ser mantidos pelo período necessário para execução do evento, prestação de contas, obrigações legais, histórico administrativo e suporte aos inscritos.
    </p>

    <h2>7. Direitos do titular</h2>
    <p>
        O titular pode solicitar informações, correção ou orientação sobre o tratamento de seus dados entrando em contato com a equipe organizadora.
    </p>

    <h2>8. Contato</h2>
    <p>
        Para dúvidas sobre privacidade ou tratamento de dados, envie uma mensagem para
        <a href="mailto:paroquiasaodomingos.carmo@gmail.com">paroquiasaodomingos.carmo@gmail.com</a>.
    </p>

